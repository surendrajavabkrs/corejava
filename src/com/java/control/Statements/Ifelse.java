package com.java.control.Statements;

public class Ifelse {
	public static void main(String[] args) {
		int a = 2;

		if(a>2) {
			System.out.println("a is greater than 2");
		}
		else if(a<2) {
			System.out.println("a is less than 2");
		}
		else {
			System.out.println("a is not greater than or less than 2");
		}
	}
}
